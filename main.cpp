#include <iostream>
using namespace std;

int main() {
    int n, h, r;

    cout << "Numero blocchi: ";
    cin >> n;
    h = 0;

while (n > 0) {
    
        if (n % 2 == 0) {
            r = 0;
        } else {
            r = 1;
        }
}
    cout << "L'altezza è pari a " << h << endl;
    cout << "Il resto è uguale a " << r << endl;


}
